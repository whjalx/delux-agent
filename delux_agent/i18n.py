DEFAULT_LANG = "en"


class I18n:
    def __init__(self, lang: str = DEFAULT_LANG) -> None:
        self._lang = lang if lang in TRANSLATIONS else DEFAULT_LANG

    @property
    def lang(self) -> str:
        return self._lang

    @lang.setter
    def lang(self, value: str) -> None:
        if value in TRANSLATIONS:
            self._lang = value

    def t(self, key: str) -> str:
        return TRANSLATIONS.get(self._lang, {}).get(key, TRANSLATIONS.get(DEFAULT_LANG, {}).get(key, key))

    def available(self) -> list[tuple[str, str]]:
        return list(LANGUAGES.items())
